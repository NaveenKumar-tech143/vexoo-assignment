def classify_query(query):
    query = query.lower()

    if any(word in query for word in ["calculate", "solve", "math", "number"]):
        return "math"
    elif any(word in query for word in ["law", "legal", "act"]):
        return "legal"
    else:
        return "general"


def reasoning_router(query, pyramid):
    qtype = classify_query(query)

    if qtype == "math":
        return "🔢 Routed to Math Engine (simulated)", qtype

    elif qtype == "legal":
        return "⚖️ Routed to Legal Rule System (simulated)", qtype

    else:
        result, layer, score = retrieve(query, pyramid)
        return f"📚 Retrieved from {layer}: {result}", qtype


