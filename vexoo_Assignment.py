import re
from collections import Counter
from difflib import SequenceMatcher

# Optional imports for Part 2
try:
    from datasets import load_dataset
    from transformers import AutoTokenizer, AutoModelForCausalLM, TrainingArguments, Trainer
    HF_AVAILABLE = True
except:
    HF_AVAILABLE = False


# =========================================================
# PART 1: SLIDING WINDOW + KNOWLEDGE PYRAMID
# =========================================================

def sliding_window(text, window_size=1000, overlap=200):
    chunks = []
    i = 0
    while i < len(text):
        chunk = text[i:i + window_size]
        chunks.append(chunk)
        i += (window_size - overlap)
    return chunks


def summarize(text):
    sentences = text.split(".")
    return ".".join(sentences[:2])


def categorize(text):
    text = text.lower()
    if "price" in text or "cost" in text:
        return "Finance"
    elif "model" in text or "ai" in text:
        return "Technology"
    elif "law" in text:
        return "Legal"
    else:
        return "General"


def extract_keywords(text, top_n=5):
    words = re.findall(r'\w+', text.lower())
    common = Counter(words).most_common(top_n)
    return [word for word, _ in common]


def build_pyramid(chunks):
    pyramid = []

    for chunk in chunks:
        pyramid.append({
            "raw": chunk,
            "summary": summarize(chunk),
            "category": categorize(chunk),
            "keywords": extract_keywords(chunk)
        })

    return pyramid


def similarity(a, b):
    return SequenceMatcher(None, a, b).ratio()


def retrieve(query, pyramid):
    best_score = 0
    best_result = None
    best_layer = None

    for item in pyramid:
        for key in ["raw", "summary", "category", "keywords"]:
            value = str(item[key])
            score = similarity(query.lower(), value.lower())

            if score > best_score:
                best_score = score
                best_result = value
                best_layer = key

    return best_result, best_layer, best_score


