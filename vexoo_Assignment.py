def main():
    print("\n==============================")
    print(" VEXOO LABS ASSIGNMENT RUNNER ")
    print("==============================")

    # -----------------------------
    # Load Document
    # -----------------------------
    try:
        with open("sample.txt", "r", encoding="utf-8") as f:
            text = f.read()
    except:
        print("\n⚠️ sample.txt not found. Using default text.\n")
        text = """Artificial Intelligence is transforming industries.
        The cost of AI models is decreasing rapidly.
        Legal frameworks are evolving to regulate AI systems."""

    # -----------------------------
    # Part 1 Execution
    # -----------------------------
    print("\n📄 Running Part 1: Knowledge Pyramid...")

    chunks = sliding_window(text)
    pyramid = build_pyramid(chunks)

    print(f"✅ Created {len(pyramid)} pyramid entries")

    # Query loop
    while True:
        query = input("\nEnter query (or 'exit'): ")

        if query.lower() == "exit":
            break

        response, qtype = reasoning_router(query, pyramid)
        print("\nResponse:", response)
        print("Query Type:", qtype)

    # -----------------------------
    # Part 2 Execution
    # -----------------------------
    choice = input("\nRun Part 2 Training? (yes/no): ")

    if choice.lower() == "yes":
        run_training()


# =========================================================

if __name__ == "__main__":
    main()

