# PART 2: GSM8K TRAINING (SIMULATED LIGHTWEIGHT VERSION)
# =========================================================

def run_training():
    if not HF_AVAILABLE:
        print("\n⚠️ HuggingFace libraries not installed.")
        print("Run: pip install transformers datasets torch\n")
        return

    print("\n📥 Loading GSM8K dataset...")
    dataset = load_dataset("openai/gsm8k", "main")

    train_data = dataset["train"].select(range(3000))
    test_data = dataset["test"].select(range(1000))

    model_name = "distilgpt2"  # lightweight substitute

    tokenizer = AutoTokenizer.from_pretrained(model_name)
    tokenizer.pad_token = tokenizer.eos_token

    def tokenize(example):
        return tokenizer(
            example["question"] + " " + example["answer"],
            truncation=True,
            padding="max_length",
            max_length=128
        )

    print("🔄 Tokenizing...")
    train_data = train_data.map(tokenize, batched=True)
    test_data = test_data.map(tokenize, batched=True)

    print("🤖 Loading model...")
    model = AutoModelForCausalLM.from_pretrained(model_name)

    training_args = TrainingArguments(
        output_dir="./results",
        per_device_train_batch_size=4,
        num_train_epochs=1,
        logging_steps=50,
        save_steps=500,
        evaluation_strategy="epoch"
    )

    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=train_data,
        eval_dataset=test_data
    )

    print("\n🚀 Training started...")
    trainer.train()

    print("\n📊 Evaluating...")
    results = trainer.evaluate()
    print("Evaluation Results:", results)


